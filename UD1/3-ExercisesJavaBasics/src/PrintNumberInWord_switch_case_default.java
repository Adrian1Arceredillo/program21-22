public class PrintNumberInWord_switch_case_default {
    public static void main(String[] args) {
        
        int number = 2;
        
        switch (number) {
            case 1:
                System.out.println("ONE");
                break;
            case 2:
                System.out.println("TWO");
                break;
            case 3:
                System.out.println("THREE");
                break;
            case 4:
                System.out.println("FOUR");
                break;
            case 5:
                System.out.println("FIVE");
                break;
    
        }
        
    }
}
