    private void jBotonIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBotonIgualActionPerformed
        // TODO add your handling code here:

        String valorDos = jTextoPantalla.getText();
        
        String separatorSuma = "+";
        int posSeparatorSuma = jTextoPantalla.getText().indexOf(separatorSuma);
        
        String separatorResta = "-";
        int posSeparatorResta = jTextoPantalla.getText().indexOf(separatorResta);
        
        String separatorMulti = "x";
        int posSeparatorMulti = jTextoPantalla.getText().indexOf(separatorMulti);
        
        String separatorDiv = "/";
        int posSeparatorDiv = jTextoPantalla.getText().indexOf(separatorDiv);
        
        
        int resultadoOperacion = 0;

        /*zenbakitzailea = Integer.valueOf(valorDos.substring(0, posSeparator));
        izendatzailea = Integer.valueOf(valorDos.substring(posSeparator + separator.length()));*/
        if (jBotonSuma.getText() != " " && jBotonResta.getText() == " " && jBotonMulti.getText() == " " && jBotonDiv.getText() == " ") {
            jSegundoValor.setText(valorDos.substring(posSeparatorSuma + separatorSuma.length()));
            resultadoOperacion = (Integer.valueOf(jPrimerValor.getText())) + (Integer.valueOf(jSegundoValor.getText()));
            jTextoPantalla.setText(Integer.toString(resultadoOperacion));
        } else if (jBotonResta.getText() != " " && jBotonSuma.getText() == " " && jBotonMulti.getText() == " " && jBotonDiv.getText() == " ") {
            jSegundoValor.setText(valorDos.substring(posSeparatorResta + separatorResta.length()));
            resultadoOperacion = (Integer.valueOf(jPrimerValor.getText())) - (Integer.valueOf(jSegundoValor.getText()));
            jTextoPantalla.setText(Integer.toString(resultadoOperacion));
        } else if (jBotonMulti.getText() != " " && jBotonSuma.getText() == " " && jBotonResta.getText() == " " && jBotonDiv.getText() == " ") {
            jSegundoValor.setText(valorDos.substring(posSeparatorMulti + separatorMulti.length()));
            resultadoOperacion = (Integer.valueOf(jPrimerValor.getText())) * (Integer.valueOf(jSegundoValor.getText()));
            jTextoPantalla.setText(Integer.toString(resultadoOperacion));
        } else if (jBotonDiv.getText() != " " && jBotonSuma.getText() == " " && jBotonResta.getText() == " " && jBotonMulti.getText() == " ") {
            jSegundoValor.setText(valorDos.substring(posSeparatorDiv + separatorDiv.length()));
            resultadoOperacion = (Integer.valueOf(jPrimerValor.getText()) / (Integer.valueOf(jSegundoValor.getText())));
            jTextoPantalla.setText(Integer.toString(resultadoOperacion));
        }

    }//GEN-LAST:event_jBotonIgualActionPerformed

